package strings;

public class StringConcat {
public static void main(String[] args) {
	
	String s1 = "Hello";
	
	int i = 10;
	int j = 2;
	
	System.out.println(i+j);
	System.out.println(s1);
	System.out.println(i+j+s1);
	System.out.println(s1+i+j);
	System.out.println(s1+(i+j));
	
	
	int v= 100;
	String convert = v+"";
	System.out.println(convert);
	
	
}
}
